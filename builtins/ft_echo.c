#include "../includes/minishell.h"

void	ft_echo(char **tab)
{
	int		i;

	i = 1;
	while (tab[i])
	{
		ft_putstr(tab[i]);
		if (tab[i + 1])
			ft_putchar(' ');
		i++;
	}
	ft_putendl("");
}
